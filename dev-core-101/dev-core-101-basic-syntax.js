// Объявляем две переменные
let a = 10;
let b = 5;

// Сложение
let resultAdd = a + b;
console.log("Сложение:", resultAdd); // 15

// Вычитание
let resultSub = a - b;
console.log("Вычитание:", resultSub); // 5

// Умножение
let resultMul = a * b;
console.log("Умножение:", resultMul); // 50

// Деление
let resultDiv = a / b;
console.log("Деление:", resultDiv); // 2
