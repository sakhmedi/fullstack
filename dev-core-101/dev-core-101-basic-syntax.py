# Объявляем две переменные
a = 10
b = 5

# Сложение
result_add = a + b
print("Сложение:", result_add)  # 15

# Вычитание
result_sub = a - b
print("Вычитание:", result_sub)  # 5

# Умножение
result_mul = a * b
print("Умножение:", result_mul)  # 50

# Деление
result_div = a / b
print("Деление:", result_div)  # 2.0
