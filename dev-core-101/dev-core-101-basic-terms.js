// 1. Переменная (variable)
let userAge = 21;
console.log("Возраст пользователя:", userAge);

// 2. Функция (function)
function greet(name) {
    console.log("Привет, " + name + "!");
}
greet("Салима");

// 3. Условный оператор (if statement)
if (userAge >= 18) {
    console.log("Пользователь совершеннолетний");
} else {
    console.log("Пользователь несовершеннолетний");
}

// 4. Цикл (loop)
for (let i = 0; i < 3; i++) {
    console.log("Сообщение номер:", i);
}

// 5. Оператор присваивания (assignment operator)
let x = 5;
x = x + 10; // новое значение
console.log("Новое значение x:", x);

// 6. Выражение (expression)
let result = (2 + 3) * 4;
console.log("Результат выражения:", result);

// 7. Целые числа (integers)
let day = 7;
console.log("Номер дня недели:", day);

// 8. Строки (strings)
let city = "Astana";
console.log("Город:", city);

// 9. Комментарии (comments)
// Это комментарий, он не выполняется
console.log("Комментарий выше не выполнялся");

// 10. Типы данных (data types)
let isStudent = true;
console.log("Студент?", isStudent);
