# 1. Переменная (variable)
user_age = 21
print("Возраст пользователя:", user_age)

# 2. Функция (function)
def greet(name):
    print("Привет,", name + "!")
greet("Салима")

# 3. Условный оператор (if statement)
if user_age >= 18:
    print("Пользователь совершеннолетний")
else:
    print("Пользователь несовершеннолетний")

# 4. Цикл (loop)
for i in range(3):
    print("Сообщение номер:", i)

# 5. Оператор присваивания (assignment operator)
x = 5
x = x + 10  # присвоили новое значение
print("Новое значение x:", x)

# 6. Выражение (expression)
result = (2 + 3) * 4
print("Результат выражения:", result)

# 7. Целые числа (integers)
day = 7
print("Номер дня недели:", day)

# 8. Строки (strings)
city = "Astana"
print("Город:", city)

# 9. Комментарии (comments)
# Этот код показывает пример комментария
print("Комментарий выше не выполнялся")

# 10. Типы данных (data types)
is_student = True
print("Студент?", is_student)
